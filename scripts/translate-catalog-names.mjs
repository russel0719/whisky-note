const escapeRe = (s) => s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');

function translate(name) {
  let s = name.replace(/’/g, "'");

  // 브랜드 접두 매칭 (긴 이름 우선)
  const brand = Object.keys(BRANDS)
    .sort((a, b) => b.length - a.length)
    .find((b) => s === b || s.startsWith(`${b} `));
  if (brand) s = BRANDS[brand] + s.slice(brand.length);

  // 숙성 연수 / 주년
  s = s.replace(/(\d+(?:\.\d+)?) Year Old/g, '$1년');
  s = s.replace(/(\d+(?:\.\d+)?) Years?\b/g, '$1년');
  s = s.replace(/(\d+)(?:st|nd|rd|th) Anniversary/g, '$1주년');

  // 용어 대역 (긴 구절 우선, 단어 경계)
  for (const term of Object.keys(TERMS).sort((a, b) => b.length - a.length)) {
    s = s.replace(new RegExp(`(?<![A-Za-z])${escapeRe(term)}(?![A-Za-z])`, 'g'), TERMS[term]);
  }

  return s.replace(/\s+/g, ' ').replace(/\s+\(/g, ' (').trim();
}

const seedSql = await readFile(SEED, 'utf8');
const names = [...seedSql.matchAll(/^\('((?:[^']|'')*)'/gm)].map((m) => m[1].replace(/''/g, "'"));
console.log(`${names.length}개 제품명 변환`);

const q = (s) => `'${String(s).replace(/'/g, "''")}'`;
let sql = `-- 카탈로그 한글 표기 (scripts/translate-catalog-names.mjs — 사전 기반 직접 번역)\n\nALTER TABLE whisky_note.catalog ADD COLUMN name_ko text;\n\n-- 시드 데이터 오타 수정\nUPDATE whisky_note.catalog SET name = 'Auchentoshan 12 Year Old' WHERE name = 'Auchentoship 12 Year Old';\n\n`;

let untranslated = 0;
for (const name of names) {
  const source = name === 'Auchentoship 12 Year Old' ? 'Auchentoshan 12 Year Old' : name;
  const ko = translate(name);
  if (ko === name) untranslated += 1;
  sql += `UPDATE whisky_note.catalog SET name_ko = ${q(ko)} WHERE name = ${q(source)};\n`;
}

